void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
    if (htim->Instance == TIM4) {
        fill_sound_buffer(); // Call to fill buffer on timer interrupt
    }
    else if(htim->Instance == TIM2){
    	HAL_ADC_Start_IT(&hadc1);
		HAL_TIM_Base_Start_IT(&htim2);
    }
}

void fill_sound_buffer() {
	 if (buffer_index < INT_BUF_SIZE) {
	        sound_buffer[buffer_index++] = (HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_1) == GPIO_PIN_SET) ? 1 : 0; // Assign 1 for high and 0 for low
	    } else {
	        buffer_index = 0; // Reset index if buffer is full (optional: handle full buffer case)
	    }
}

void servoIntensity(){
	int high_count = 0; // Count of high values (1s) in the buffer

	// Count the number of high values (1s) in the sound buffer
	for (int i = 0; i < INT_BUF_SIZE; i++) {
		if (sound_buffer[i] == 1) {
			high_count++;
		}
	}

	// Calculate the percentage of high values
	float high_percentage = (float)high_count / INT_BUF_SIZE * 100;

	// Ensure that high_percentage is within the valid range (0 to 100)
	if (high_percentage < 0) {
		high_percentage = 0;
	} else if (high_percentage > 100) {
		high_percentage = 100;
	}

//	if (high_percentage < 20) {
//		printf("Sound Level= LOW\r\n");
//	} else if (high_percentage >= 20 && high_percentage < 65) {
//		printf("Sound Level = MEDIUM\r\n");
//	} else if (high_percentage >= 65){
//		printf("Sound Level = HIGH\r\n");
//	}

	// Normalize to a duty cycle value for servo control
	int dutyCycle = 125 - (int)((high_percentage * (125 - 25)) / 100); // Map percentage to duty cycle range
	htim3.Instance->CCR1 = dutyCycle; // Set the compare register to the duty cycle
	HAL_Delay(1000);
}

