void fill_mic_buffer(){
	for(int i=0; i<DATA_INPUT_USER; i++){
	mic_buffer[AXIS_NUMBER * i] = mic;
	HAL_Delay(3);
	}
}

void Log(){
	fill_mic_buffer();
	for(int i=0; i<DATA_INPUT_USER; i++){
		printf("%.2f", mic_buffer[AXIS_NUMBER * i]);
		printf("\t");
	}
	printf("\r\n");
}

void Inference(){
	uint16_t i, id_class_t0, id_class_tn;
	fill_mic_buffer();
	neai_classification(mic_buffer, output_class_buffer, &id_class_t0);
	for(i=0; i<CONFIRMATIONS_NB-1; i++){
		fill_mic_buffer();
		neai_classification(mic_buffer, output_class_buffer, &id_class_tn);
		if(id_class_t0 != id_class_tn){
			printf("Detected Class:Noise");
			printf("\r\n");
			ssd1306_UpdateScreen();
			ssd1306_SetCursor(2, 0);
			ssd1306_WriteString("Detected Sound:", Font_7x10, White);
			ssd1306_SetCursor(2, 18);
			ssd1306_WriteString("             ", Font_11x18, White);
			ssd1306_SetCursor(2, 18);
			ssd1306_WriteString("Noise", Font_11x18, White);
			ssd1306_UpdateScreen();
			break;
		}
		else if(id_class_t0 == id_class_tn){
					printf("Detected Class:");
					printf(id2class[id_class_t0]);
					printf("\r\n");
					ssd1306_UpdateScreen();
					ssd1306_SetCursor(2, 0);
					ssd1306_WriteString("Detected Sound:", Font_7x10, White);
					ssd1306_SetCursor(2, 18);
					ssd1306_WriteString("             ", Font_11x18, White);
					ssd1306_SetCursor(2, 18);
					ssd1306_WriteString(id2class[id_class_t0], Font_11x18, White);
					ssd1306_UpdateScreen();
		}
		else{
			printf("?");
			printf("\r\n");
			ssd1306_UpdateScreen();
			ssd1306_SetCursor(2, 0);
			ssd1306_WriteString("Detected Sound:", Font_7x10, White);
			ssd1306_SetCursor(2, 18);
			ssd1306_WriteString("             ", Font_11x18, White);
			ssd1306_SetCursor(2, 18);
			ssd1306_WriteString("Noise", Font_11x18, White);
			ssd1306_UpdateScreen();
		}
	}
}

